    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // 1. Consultamos mediante la funcion GetText() el valor que el usuario ingreso
        // en el campo de usuario y de contraseña
        String username = txtUsuario.getText();
        String password = txtPassword.getText();
        System.out.println("username: " + username + ", Password: " + password );
        // 2. validar si alguno de los 2 campos esta vacio
        if (username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar.", "Login",JOptionpane.WARNING_MESSAGE);
        }else{
            if (username.equals("MisionTIC") && password.equals("Ciclo2")){
                JOptionPane.showMessageDialog(this, "Bienvenido al sistema.");
                // 4. Crear una instancia de la vista Usermenu
                UserMenu userMenu = new UserMenu();
                // 5. Hacer visible la vista mediante el metodo setVisible
                userMenu.setVisible(true);
                // 6. Eliminar Vista De Login
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Error. Revisa los valores suministrados.", "Login",JOptionpane.ERROR_MESSAGE);
                
            }
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

